package br.com.db1.exercicio7;

public class Sede {
	private String nomeCidade;
	private Pais pais;
	
	public void setNomeCidade(String nomeCidade) {
		this.nomeCidade = nomeCidade;
	}
	
	public String getNomeCidade() {
		return nomeCidade;
	}
	
	public void setPais(Pais pais) {
		this.pais = pais;
	}
	
	public Pais getPais() {
		return pais;
	}
}
